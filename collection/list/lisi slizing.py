
lst=[10,20,30,50,40,80,60,90,100,30]

# positive indexing

print(lst[0])
print(lst[7])
print(lst[1:4])  #important
print(lst[0:4])

print(lst[:2])
print(lst[3:])
print(lst[:])

# negative indexing
# -1=-n
print(lst[-1])
print(lst[-4:-1])
print(lst[-1:-4])   #not logically possible
print(lst[-3:])
print(lst[:-4])